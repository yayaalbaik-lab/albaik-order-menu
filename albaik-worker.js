export default {
  async fetch(request, env) {
    const corsHeaders = {
      "Access-Control-Allow-Origin": env.ALLOWED_ORIGIN || "*",
      "Access-Control-Allow-Methods": "POST, OPTIONS",
      "Access-Control-Allow-Headers": "Content-Type",
      "Access-Control-Max-Age": "86400",
    };

    if (request.method === "OPTIONS") {
      return new Response(null, { status: 204, headers: corsHeaders });
    }

    if (request.method !== "POST") {
      return jsonResponse({ message: "Method not allowed" }, 405, corsHeaders);
    }

    try {
      if (!env.TELEGRAM_BOT_TOKEN || !env.TELEGRAM_CHAT_ID) {
        return jsonResponse({ message: "Telegram is not configured" }, 500, corsHeaders);
      }

      const body = await request.json();
      const order = normalizeOrder(body);
      const pricedOrder = priceOrder(order);
      const orderNumber = createDemoOrderNumber();
      const message = formatTelegramMessage(orderNumber, pricedOrder);

      const telegramResponse = await fetch(
        `https://api.telegram.org/bot${env.TELEGRAM_BOT_TOKEN}/sendMessage`,
        {
          method: "POST",
          headers: { "Content-Type": "application/json" },
          body: JSON.stringify({
            chat_id: env.TELEGRAM_CHAT_ID,
            text: message,
            parse_mode: "HTML",
            disable_web_page_preview: true,
          }),
        }
      );

      const telegramResult = await telegramResponse.json().catch(() => ({}));

      if (!telegramResponse.ok || telegramResult.ok === false) {
        return jsonResponse(
          { message: "Telegram send failed", details: telegramResult.description || "Unknown error" },
          502,
          corsHeaders
        );
      }

      return jsonResponse(
        {
          ok: true,
          order_number: orderNumber,
          total: pricedOrder.total,
          telegram_status: "sent_to_telegram",
        },
        200,
        corsHeaders
      );
    } catch (error) {
      return jsonResponse(
        { message: error.message || "Invalid order" },
        400,
        corsHeaders
      );
    }
  },
};

function normalizeOrder(body) {
  const customerName = cleanText(body.customer_name, 40);
  const customerNote = cleanText(body.customer_note || "", 160);
  const language = body.language === "en" ? "en" : "ar";

  if (!customerName) {
    throw new Error("Customer name is required");
  }

  if (!Array.isArray(body.items) || body.items.length === 0) {
    throw new Error("Cart is empty");
  }

  if (body.items.length > 30) {
    throw new Error("Too many items");
  }

  const items = body.items.map((item) => {
    const productId = cleanText(item.product_id, 80);
    const quantity = Number(item.quantity);
    const itemNote = cleanText(item.item_note || "", 120);

    if (!productId) throw new Error("Invalid product");
    if (!Number.isInteger(quantity) || quantity < 1 || quantity > 9) {
      throw new Error("Invalid quantity");
    }

    return { product_id: productId, quantity, item_note: itemNote };
  });

  return { customer_name: customerName, customer_note: customerNote, language, items };
}

function priceOrder(order) {
  let total = 0;
  const items = order.items.map((item) => {
    const product = PRODUCT_CATALOG[item.product_id];
    if (!product) {
      throw new Error(`Unknown product: ${item.product_id}`);
    }

    const unitPrice = Number(product.price);
    const lineTotal = roundMoney(unitPrice * item.quantity);
    total = roundMoney(total + lineTotal);

    return {
      ...item,
      name_ar: product.name_ar,
      name_en: product.name_en,
      unit_price: unitPrice,
      line_total: lineTotal,
    };
  });

  return { ...order, items, total };
}

function formatTelegramMessage(orderNumber, order) {
  const AR = {
    newOrder: "\u0637\u0644\u0628 \u062C\u062F\u064A\u062F",
    time: "\u0627\u0644\u0648\u0642\u062A",
    name: "\u0627\u0644\u0627\u0633\u0645",
    items: "\u0627\u0644\u0623\u0635\u0646\u0627\u0641",
    orderNotes: "\u0645\u0644\u0627\u062D\u0638\u0627\u062A \u0627\u0644\u0637\u0644\u0628",
    total: "\u0627\u0644\u0645\u062C\u0645\u0648\u0639",
    jod: "\u062F.\u0623",
  };

  const lines = [];
  const now = new Date();
  const time = new Intl.DateTimeFormat("ar-JO", {
    timeZone: "Asia/Amman",
    dateStyle: "short",
    timeStyle: "short",
  }).format(now);

  lines.push("<b>" + AR.newOrder + " #" + escapeHtml(orderNumber) + "</b>");
  lines.push("");
  lines.push("<b>" + AR.time + ":</b> " + escapeHtml(time));
  lines.push("<b>" + AR.name + ":</b> " + escapeHtml(order.customer_name));
  lines.push("");
  lines.push("<b>" + AR.items + ":</b>");

  for (const item of order.items) {
    lines.push("- " + item.quantity + " x " + escapeHtml(item.name_ar) + " - " + formatMoney(item.line_total) + " " + AR.jod);
    if (item.item_note) {
      lines.push("  > " + escapeHtml(item.item_note));
    }
  }

  if (order.customer_note) {
    lines.push("");
    lines.push("<b>" + AR.orderNotes + ":</b>");
    lines.push(escapeHtml(order.customer_note));
  }

  lines.push("");
  lines.push("<b>" + AR.total + ":</b> " + formatMoney(order.total) + " " + AR.jod);

  return lines.join("\n");
}
function createDemoOrderNumber() {
  const now = new Date();
  const yyyy = now.getFullYear();
  const mm = String(now.getMonth() + 1).padStart(2, "0");
  const dd = String(now.getDate()).padStart(2, "0");
  const suffix = String(Math.floor(Math.random() * 900) + 100);
  return `${yyyy}${mm}${dd}-${suffix}`;
}

function cleanText(value, maxLength) {
  return String(value || "")
    .replace(/[\u0000-\u001F\u007F]/g, " ")
    .replace(/\s+/g, " ")
    .trim()
    .slice(0, maxLength);
}

function roundMoney(value) {
  return Math.round((Number(value) + Number.EPSILON) * 1000) / 1000;
}

function formatMoney(value) {
  return Number(value || 0).toFixed(2).replace(/\.00$/, "");
}

function escapeHtml(value) {
  return String(value || "")
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;")
    .replace(/"/g, "&quot;");
}

function jsonResponse(data, status, headers) {
  return new Response(JSON.stringify(data), {
    status,
    headers: {
      ...headers,
      "Content-Type": "application/json; charset=utf-8",
    },
  });
}

