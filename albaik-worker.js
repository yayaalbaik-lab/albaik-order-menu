export default {
  async fetch(request, env) {
    const corsHeaders = createCorsHeaders(request, env);
    const url = new URL(request.url);

    if (request.method === "OPTIONS") {
      return new Response(null, { status: 204, headers: corsHeaders });
    }

    if (url.pathname === "/cashier/orders" && request.method === "GET") {
      return handleCashierOrders(request, env, corsHeaders);
    }

    if (url.pathname === "/cashier/accept" && request.method === "POST") {
      return handleCashierAccept(request, env, corsHeaders);
    }

    if (url.pathname !== "/" || request.method !== "POST") {
      return jsonResponse({ message: "Method not allowed" }, 405, corsHeaders);
    }

    try {
      if (!env.TELEGRAM_BOT_TOKEN || !env.TELEGRAM_CHAT_ID) {
        return jsonResponse({ message: "Telegram is not configured" }, 500, corsHeaders);
      }

      const contentLength = Number(request.headers.get("content-length") || 0);
      if (contentLength > 20_000) {
        return jsonResponse({ message: "Request is too large" }, 413, corsHeaders);
      }

      const body = await request.json();
      const order = normalizeOrder(body);
      await enforceRateLimit(request, env, order.device_token);
      const pricedOrder = priceOrder(order);
      const orderNumber = createDemoOrderNumber();
      await saveCashierOrder(env, orderNumber, pricedOrder);
      const message = formatTelegramMessage(orderNumber, pricedOrder);
      let telegramStatus = "sent_to_telegram";

      const telegramResponse = await fetch(
        `https://api.telegram.org/bot${env.TELEGRAM_BOT_TOKEN}/sendMessage`,
        {
          method: "POST",
          headers: { "Content-Type": "application/json" },
          body: JSON.stringify({
            chat_id: env.TELEGRAM_CHAT_ID,
            text: message,
            parse_mode: "HTML",
            disable_web_page_preview: true,
          }),
        }
      );

      const telegramResult = await telegramResponse.json().catch(() => ({}));

      if (!telegramResponse.ok || telegramResult.ok === false) {
        telegramStatus = "telegram_failed";
      }

      return jsonResponse(
        {
          ok: true,
          order_number: orderNumber,
          total: pricedOrder.total,
          telegram_status: telegramStatus,
        },
        200,
        corsHeaders
      );
    } catch (error) {
      if (error.code === "RATE_LIMITED") {
        return jsonResponse(
          { message: "RATE_LIMITED" },
          429,
          { ...corsHeaders, "Retry-After": String(RATE_LIMIT_WINDOW_SECONDS) }
        );
      }

      return jsonResponse(
        { message: error.message || "Invalid order" },
        error.status || 400,
        corsHeaders
      );
    }
  },
};

const RATE_LIMIT_MAX_ORDERS = 2;
const RATE_LIMIT_WINDOW_SECONDS = 10 * 60;
const CASHIER_ORDER_TTL_SECONDS = 24 * 60 * 60;

function createCorsHeaders(request, env) {
  const requestOrigin = request.headers.get("Origin") || "";
  const configuredOrigins = cleanText(env.ALLOWED_ORIGINS || env.ALLOWED_ORIGIN || "", 500)
    .split(",")
    .map((origin) => origin.trim())
    .filter(Boolean);
  const allowOrigin = configuredOrigins.length === 0
    ? "*"
    : configuredOrigins.includes(requestOrigin)
      ? requestOrigin
      : configuredOrigins[0];

  return {
    "Access-Control-Allow-Origin": allowOrigin,
    "Access-Control-Allow-Methods": "GET, POST, OPTIONS",
    "Access-Control-Allow-Headers": "Content-Type, Authorization",
    "Access-Control-Max-Age": "86400",
  };
}

function normalizeOrder(body) {
  const customerName = cleanText(body.customer_name, 40);
  const customerNote = cleanText(body.customer_note || "", 160);
  const deviceToken = cleanText(body.device_token || "", 80);
  const language = body.language === "en" ? "en" : "ar";

  if (!customerName) {
    throw new Error("Customer name is required");
  }

  if (!Array.isArray(body.items) || body.items.length === 0) {
    throw new Error("Cart is empty");
  }

  if (body.items.length > 30) {
    throw new Error("Too many items");
  }

  const items = body.items.map((item) => {
    const productId = cleanText(item.product_id, 80);
    const quantity = Number(item.quantity);
    const itemNote = cleanText(item.item_note || "", 120);

    if (!productId) throw new Error("Invalid product");
    if (!Number.isInteger(quantity) || quantity < 1 || quantity > 20) {
      throw new Error("Invalid quantity");
    }

    return { product_id: productId, quantity, item_note: itemNote };
  });

  return { customer_name: customerName, customer_note: customerNote, device_token: deviceToken, language, items };
}

async function enforceRateLimit(request, env, deviceToken) {
  if (!env.ORDER_RATE_LIMIT_KV) {
    const error = new Error("Rate limit storage is not configured");
    error.status = 500;
    throw error;
  }

  const ip =
    request.headers.get("CF-Connecting-IP") ||
    request.headers.get("X-Forwarded-For") ||
    "unknown-ip";
  const userAgent = request.headers.get("User-Agent") || "unknown-agent";
  const identityHash = await sha256([ip, userAgent, deviceToken || "no-device-token"].join("|"));
  const key = "orders:" + identityHash;
  const now = Date.now();
  const windowStart = now - RATE_LIMIT_WINDOW_SECONDS * 1000;

  const existing = await env.ORDER_RATE_LIMIT_KV.get(key, { type: "json" }).catch(() => null);
  const recentOrders = Array.isArray(existing)
    ? existing.filter((timestamp) => Number(timestamp) >= windowStart)
    : [];

  if (recentOrders.length >= RATE_LIMIT_MAX_ORDERS) {
    const error = new Error("RATE_LIMITED");
    error.code = "RATE_LIMITED";
    throw error;
  }

  recentOrders.push(now);
  await env.ORDER_RATE_LIMIT_KV.put(key, JSON.stringify(recentOrders), {
    expirationTtl: RATE_LIMIT_WINDOW_SECONDS,
  });
}

async function handleCashierOrders(request, env, corsHeaders) {
  try {
    requireCashierAccess(request, env);
    const store = getOrderStore(env);
    const list = await store.list({ prefix: "cashier:order:" });
    const orders = [];

    for (const key of list.keys) {
      const order = await store.get(key.name, { type: "json" }).catch(() => null);
      if (order && order.status === "new") {
        orders.push(publicCashierOrder(order));
      }
    }

    orders.sort((a, b) => new Date(a.created_at).getTime() - new Date(b.created_at).getTime());
    return jsonResponse({ ok: true, orders }, 200, corsHeaders);
  } catch (error) {
    return jsonResponse({ message: error.message || "Unauthorized" }, error.status || 401, corsHeaders);
  }
}

async function handleCashierAccept(request, env, corsHeaders) {
  try {
    requireCashierAccess(request, env);
    const body = await request.json().catch(() => ({}));
    const orderId = cleanText(body.order_id, 80);
    if (!orderId) {
      return jsonResponse({ message: "Missing order id" }, 400, corsHeaders);
    }

    const store = getOrderStore(env);
    const key = "cashier:order:" + orderId;
    const order = await store.get(key, { type: "json" }).catch(() => null);
    if (!order) {
      return jsonResponse({ message: "Order not found" }, 404, corsHeaders);
    }

    order.status = "accepted";
    order.accepted_at = new Date().toISOString();
    await store.put(key, JSON.stringify(order), { expirationTtl: CASHIER_ORDER_TTL_SECONDS });

    return jsonResponse({ ok: true, order_id: orderId }, 200, corsHeaders);
  } catch (error) {
    return jsonResponse({ message: error.message || "Unauthorized" }, error.status || 401, corsHeaders);
  }
}

async function saveCashierOrder(env, orderNumber, order) {
  const store = getOrderStore(env);
  const createdAt = new Date().toISOString();
  const cashierOrder = {
    id: orderNumber,
    status: "new",
    created_at: createdAt,
    customer_name: order.customer_name,
    customer_note: order.customer_note,
    total: order.total,
    items: order.items.map((item) => ({
      product_id: item.product_id,
      quantity: item.quantity,
      item_note: item.item_note,
      name_ar: item.name_ar,
      unit_price: item.unit_price,
      line_total: item.line_total,
    })),
  };

  await store.put("cashier:order:" + orderNumber, JSON.stringify(cashierOrder), {
    expirationTtl: CASHIER_ORDER_TTL_SECONDS,
  });
}

function publicCashierOrder(order) {
  return {
    id: order.id,
    created_at: order.created_at,
    customer_name: order.customer_name,
    customer_note: order.customer_note,
    total: order.total,
    items: order.items,
  };
}

function getOrderStore(env) {
  const store = env.ORDER_STORE_KV || env.ORDER_RATE_LIMIT_KV;
  if (!store) {
    const error = new Error("Order storage is not configured");
    error.status = 500;
    throw error;
  }
  return store;
}

function requireCashierAccess(request, env) {
  if (!env.CASHIER_SECRET) {
    const error = new Error("Cashier access is not configured");
    error.status = 500;
    throw error;
  }

  const header = request.headers.get("Authorization") || "";
  const token = header.startsWith("Bearer ") ? header.slice(7).trim() : "";
  if (token !== env.CASHIER_SECRET) {
    const error = new Error("Unauthorized");
    error.status = 401;
    throw error;
  }
}

async function sha256(value) {
  const bytes = new TextEncoder().encode(value);
  const digest = await crypto.subtle.digest("SHA-256", bytes);
  return [...new Uint8Array(digest)]
    .map((byte) => byte.toString(16).padStart(2, "0"))
    .join("");
}

function priceOrder(order) {
  let total = 0;
  const items = order.items.map((item) => {
    const product = PRODUCT_CATALOG[item.product_id];
    if (!product) {
      throw new Error(`Unknown product: ${item.product_id}`);
    }

    const unitPrice = Number(product.price);
    const lineTotal = roundMoney(unitPrice * item.quantity);
    total = roundMoney(total + lineTotal);

    return {
      ...item,
      name_ar: product.name_ar,
      name_en: product.name_en,
      unit_price: unitPrice,
      line_total: lineTotal,
    };
  });

  return { ...order, items, total };
}

function formatTelegramMessage(orderNumber, order) {
  const AR = {
    newOrder: "\u0637\u0644\u0628 \u062C\u062F\u064A\u062F",
    time: "\u0627\u0644\u0648\u0642\u062A",
    name: "\u0627\u0644\u0627\u0633\u0645",
    items: "\u0627\u0644\u0623\u0635\u0646\u0627\u0641",
    orderNotes: "\u0645\u0644\u0627\u062D\u0638\u0627\u062A \u0627\u0644\u0637\u0644\u0628",
    total: "\u0627\u0644\u0645\u062C\u0645\u0648\u0639",
    jod: "\u062F.\u0623",
  };

  const lines = [];
  const now = new Date();
  const time = new Intl.DateTimeFormat("ar-JO", {
    timeZone: "Asia/Amman",
    dateStyle: "short",
    timeStyle: "short",
  }).format(now);

  lines.push("<b>" + AR.newOrder + " #" + escapeHtml(orderNumber) + "</b>");
  lines.push("");
  lines.push("<b>" + AR.time + ":</b> " + escapeHtml(time));
  lines.push("<b>" + AR.name + ":</b> " + escapeHtml(order.customer_name));
  lines.push("");
  lines.push("<b>" + AR.items + ":</b>");

  for (const item of order.items) {
    lines.push("- " + item.quantity + " x " + escapeHtml(item.name_ar) + " - " + formatMoney(item.line_total) + " " + AR.jod);
    if (item.item_note) {
      lines.push("  > " + escapeHtml(item.item_note));
    }
  }

  if (order.customer_note) {
    lines.push("");
    lines.push("<b>" + AR.orderNotes + ":</b>");
    lines.push(escapeHtml(order.customer_note));
  }

  lines.push("");
  lines.push("<b>" + AR.total + ":</b> " + formatMoney(order.total) + " " + AR.jod);

  return lines.join("\n");
}
function createDemoOrderNumber() {
  const now = new Date();
  const yyyy = now.getFullYear();
  const mm = String(now.getMonth() + 1).padStart(2, "0");
  const dd = String(now.getDate()).padStart(2, "0");
  const suffix = String(Math.floor(Math.random() * 900) + 100);
  return `${yyyy}${mm}${dd}-${suffix}`;
}

function cleanText(value, maxLength) {
  return String(value || "")
    .replace(/[\u0000-\u001F\u007F]/g, " ")
    .replace(/\s+/g, " ")
    .trim()
    .slice(0, maxLength);
}

function roundMoney(value) {
  return Math.round((Number(value) + Number.EPSILON) * 1000) / 1000;
}

function formatMoney(value) {
  return Number(value || 0).toFixed(2).replace(/\.00$/, "");
}

function escapeHtml(value) {
  return String(value || "")
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;")
    .replace(/"/g, "&quot;");
}

function jsonResponse(data, status, headers) {
  return new Response(JSON.stringify(data), {
    status,
    headers: {
      ...headers,
      "Content-Type": "application/json; charset=utf-8",
    },
  });
}

